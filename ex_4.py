import numpy as np

# Определение матрицы X
X = np.array([
    [6, 45, 2000],
    [7, 34, 2500],
    [12, 50, 4200]
])

# Определение матрицы A
A = np.array([
    [1, 0, 0],
    [0, 1, 0],
    [0, 0, 90]
])

# Вычисление новой матрицы X*
X_star = X@A

# Вывод новой матрицы X*
print(f"X*:\n{X_star}")
